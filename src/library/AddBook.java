    int bCount = 0;

    public void readBooks() {
        File f = new File("books.txt");
        try {
            Scanner s = new Scanner(f);

            while (s.hasNextLine()) {
                String x = s.nextLine();
                String tokens[] = x.split(",");
                books[bCount] = new Book(tokens[0], tokens[1], tokens[2], tokens[3], tokens[4], Integer.parseInt(tokens[5]), Integer.parseInt(tokens[6]), tokens[7]);
                bCount++;
            }
        } catch (IOException ex) {

        }
    }

    public void appendBook(String ID, String callNo, String bookName, String author, String publisher, int quantity, int issued, String addDate) throws IOException {
        File myfile = new File("Books.txt");
        FileWriter w1;

        w1 = new FileWriter(myfile, true);

        w1.append(ID + "," + callNo + "," + bookName + "," + author + "," + publisher + "," + quantity + "," + issued + "," + addDate + "\n");
        w1.close();
    }

