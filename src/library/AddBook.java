    private void addBookButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBookButtonActionPerformed
        // TODO add your handling code here:
        File myfile = new File("books.txt");
        DateFormat dateFormat = new SimpleDateFormat("dd-MM-YY");
        Date date = new Date();
        String d = dateFormat.format(date);
        int c = 0;
        int i;
        try {
            BufferedReader br = new BufferedReader(new FileReader(myfile));
            Object[] tableLines = br.lines().toArray();
            c = tableLines.length;
        } catch (IOException e) {
        }
        if (Validation.isDigit(quantityText.getText()) && !Validation.isOneDigit(authorText.getText())) {
            for (i = 0; i < bCount; i++) {
                if (books[i].getCallNo().equals(callNoText.getText())) {
                    books[i].addQuantity(Integer.parseInt(quantityText.getText()));
                }
            }

            try {
                if (c == 0) {
                    c = 1;
                }
                appendBook(String.valueOf(c), callNoText.getText(), nameText.getText(), authorText.getText(), publisherText.getText(), Integer.parseInt(quantityText.getText()), 0, d);
            } catch (IOException ex) {
                Logger.getLogger(AddBook.class.getName()).log(Level.SEVERE, null, ex);
            }

            JOptionPane.showMessageDialog(this, "Book Added");
        } else {
            JOptionPane.showMessageDialog(this, "Invalid data");
        }
        readBooks();
        // JOptionPane.showMessageDialog(this,"Book Added Successfully");
    }//GEN-LAST:event_addBookButtonActionPerformed

