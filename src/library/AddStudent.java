    public void readStudents(){
      File f = new File("Students.txt");
        try {
            Scanner s = new Scanner(f);

            while (s.hasNextLine()) {
                String x = s.nextLine();
                String tokens[] = x.split(",");
                students[sCount] = new Student(tokens[0] ,tokens[1], tokens[2], tokens[3], tokens[4],tokens[5],tokens[6],Integer.parseInt(tokens[7]));
                sCount++;
            }
        } catch (IOException ex) {}
  
    }
     public void appendStudent(String ID,String name,String password,String email,String address,String city,String contactNo,int issued) throws IOException{
        File myfile = new File("Students.txt");
        FileWriter w1 = new FileWriter(myfile,true);
        w1.append(ID+","+name+","+password+","+email+","+address+","+city+","+contactNo+","+issued+"\n");
        w1.close();
    }
