    private void addLibButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addLibButtonActionPerformed
        // TODO add your handling code here:
       boolean found = false;
       
        if(Validation.isEmailValid(emailText.getText())&&Validation.isDigit(contactText.getText())&&!Validation.isOneDigit(nameText.getText())){
        for(int i = 0 ; i<sCount;i++){
            if(students[i].getID().equals(idText.getText())){
            found = true;
            }
        }
        if(passText.getText().length()<6) JOptionPane.showMessageDialog(this,"Password can't be less than 6 charachters");
        else{   
        if(!found){
            try {
                
                appendStudent(idText.getText(),nameText.getText() ,passText.getText(), emailText.getText(),addressText.getText(),CityText.getText(),contactText.getText(),0);
            } catch (IOException ex) {
                Logger.getLogger(AddStudent.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            JOptionPane.showMessageDialog(this,"Student Added");
            }
           else JOptionPane.showMessageDialog(this,"Student Already exists");
        }
         readStudents();
        }
       
    }//GEN-LAST:event_addLibButtonActionPerformed

